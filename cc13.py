#Christmas Tree
for a in range(2,6,1):
    for b in range(0,7):
        print(" ",end=" ")
    for c in range(6,a,-1):
        print(" ",end=" ")
    for d in range(3,a):
        print("*",end=" ")
    for e in range(2,a,1):
        print("*",end=" ")
    print()    
for f in range(1,3,1):
    for g in range(0,7):
        print(" ",end=" ")
    for h in range(0 - 1,f,1):
        print(" ",end=" ")
    for i in range(2,f,-1):
        print("*",end=" ")
    for j in range(3,f,-1):
        print("*",end=" ")
    print()    
